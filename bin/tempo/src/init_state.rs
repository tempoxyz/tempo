//! Initialize state from a binary dump file.
//!
//! This command loads TIP20 storage slots from a binary file and applies them
//! to the genesis state. The binary format is produced by `tempo-xtask generate-state-bloat`.

use std::{
    fs::File,
    io::{BufReader, Read},
    path::PathBuf,
    time::{Duration, Instant},
};

use alloy_primitives::{
    B256, U256,
    map::AddressMap,
};
use clap::Parser;
use eyre::{Context as _, ensure};
use itertools::Itertools;
use reth_chainspec::EthereumHardforks;
use reth_cli_commands::common::{AccessRights, CliNodeTypes, EnvironmentArgs};
use reth_db_api::{
    cursor::{DbCursorRO, DbCursorRW},
    tables,
    transaction::DbTxMut,
};
use reth_ethereum::{chainspec::EthChainSpec, tasks::Runtime};
use reth_primitives_traits::{Account, StorageEntry};
use reth_provider::{BlockNumReader, DatabaseProviderFactory, HashingWriter};
use reth_storage_api::DBProvider;
use tempo_chainspec::spec::TempoChainSpecParser;
use tracing::info;

/// Magic bytes for the state bloat binary format (8 bytes)
const MAGIC: &[u8; 8] = b"TEMPOSB\x00";

/// Expected format version
const VERSION: u16 = 1;

/// Entries per batch before flushing hashed storage to bound heap usage. 40M ≈ 2.8 GB heap.
const HASHING_BATCH_SIZE: usize = 40_000_000;

/// Initialize state from a binary dump file.
#[derive(Debug, Parser)]
pub(crate) struct InitFromBinaryDump<C: reth_cli::chainspec::ChainSpecParser = TempoChainSpecParser>
{
    #[command(flatten)]
    env: EnvironmentArgs<C>,

    /// Path to the binary state dump file.
    ///
    /// The file should be generated by `tempo-xtask generate-state-bloat`.
    #[arg(value_name = "BINARY_DUMP_FILE")]
    state: PathBuf,
}

impl<C: reth_cli::chainspec::ChainSpecParser<ChainSpec: EthChainSpec + EthereumHardforks>>
    InitFromBinaryDump<C>
{
    /// Execute the init-from-binary-dump command.
    pub(crate) async fn execute<N>(self, runtime: Runtime) -> eyre::Result<()>
    where
        N: CliNodeTypes<ChainSpec = C::ChainSpec>,
    {
        info!(target: "tempo::cli", "Tempo init-from-binary-dump starting");

        let environment = self.env.init::<N>(AccessRights::RW, runtime)?;
        let provider_factory = environment.provider_factory;

        let provider_rw = provider_factory.database_provider_rw()?;

        // Verify we're at genesis (block 0)
        let last_block = provider_rw.last_block_number()?;
        ensure!(
            last_block == 0,
            "init-from-binary-dump must be run on a freshly initialized database at block 0, \
             but found block {last_block}"
        );

        info!(target: "tempo::cli", path = %self.state.display(), "Loading binary state dump");

        let file = File::open(&self.state)
            .wrap_err_with(|| format!("failed to open {}", self.state.display()))?;
        let mut reader = BufReader::with_capacity(64 * 1024 * 1024, file);

        let mut total_entries = 0u64;
        let mut total_blocks = 0u64;

        // Track addresses and their account data for hashing (small — only token addresses)
        let mut accounts_seen: AddressMap<Account> = AddressMap::default();

        // Batch buffer for hashed storage inserts (multi-address)
        let mut batch: AddressMap<Vec<StorageEntry>> = AddressMap::default();

        // Process blocks from binary file
        loop {
            // Read next block header; EOF means no more blocks.
            let mut header_buf = [0u8; 40];
            match reader.read_exact(&mut header_buf) {
                Ok(()) => {}
                Err(e) if e.kind() == std::io::ErrorKind::UnexpectedEof => break,
                Err(e) => return Err(e).wrap_err("failed to read block header"),
            }

            // Validate magic
            ensure!(
                &header_buf[..8] == MAGIC,
                "invalid magic bytes in block header"
            );

            // Validate version
            let version = u16::from_be_bytes([header_buf[8], header_buf[9]]);
            ensure!(
                version == VERSION,
                "unsupported binary format version {version}, expected {VERSION}"
            );

            // Skip flags (2 bytes at offset 10)

            // Read address (20 bytes at offset 12)
            let mut address_bytes = [0u8; 20];
            address_bytes.copy_from_slice(&header_buf[12..32]);
            let address = alloy_primitives::Address::from(address_bytes);

            // Read pair count (8 bytes at offset 32)
            let pair_count = u64::from_be_bytes(header_buf[32..40].try_into().unwrap());

            info!(
                target: "tempo::cli",
                %address,
                pair_count,
                "Processing token storage block"
            );

            // Flush batch if we're switching to a different address
            if batch_address.is_some() && batch_address != Some(address) && !batch.is_empty() {
                let flush_addr = batch_address.unwrap();
                provider_rw
                    .insert_storage_for_hashing([(flush_addr, batch.drain(..))])?;
                provider_rw.commit()?;
                provider_rw = provider_factory.database_provider_rw()?;
                total_commits += 1;
            }
            batch_address = Some(address);

            // Read the existing account from genesis, or insert a default empty one
            // (only on first encounter). Preserving the genesis account is critical:
            // TIP20 tokens have bytecode (0xEF) set during genesis, and overwriting with
            // Account::default() would clear the code hash.
            if !accounts_seen.contains_key(&address) {
                let tx = provider_rw.tx_ref();
                let mut account_cursor = tx.cursor_write::<tables::PlainAccountState>()?;
                let account = match account_cursor.seek_exact(address)? {
                    Some((_, account)) => account,
                    None => {
                        let account = Account::default();
                        account_cursor.upsert(address, &account)?;
                        account
                    }
                };
                accounts_seen.insert(address, account);
            }

            // Collect storage for hashing
            let mut storage_entries = batch.entry(address).or_default();

            // Read and insert entries
            let mut entry_buf = [0u8; 64];
            let start = Instant::now();
            let mut last_log = start;

            let tx = provider_rw.tx_ref();
            let mut storage_cursor = tx.cursor_dup_write::<tables::PlainStorageState>()?;

            for chunk in (0..pair_count).chunks(HASHING_BATCH_SIZE).into_iter() {
                for i in chunk {
                    reader
                        .read_exact(&mut entry_buf)
                        .wrap_err("failed to read storage entry")?;

                    let slot = B256::from_slice(&entry_buf[..32]);
                    let value = U256::from_be_bytes::<32>(entry_buf[32..64].try_into().unwrap());

                    // Skip zero values (they represent deletion)
                    if value.is_zero() {
                        continue;
                    }

                    let entry = StorageEntry { key: slot, value };

                    // Insert into plain storage state
                    storage_cursor.upsert(address, &entry)?;

                    // Collect for hashed storage
                    storage_entries.push(entry);
                    total_entries += 1;

                    let now = Instant::now();
                    if now.duration_since(last_log) >= Duration::from_secs(5) || i + 1 == pair_count
                    {
                        let pct = ((i + 1) as f64 / pair_count as f64) * 100.0;
                        let elapsed = start.elapsed();
                        let pairs_per_sec = (i + 1) as f64 / elapsed.as_secs_f64();
                        info!(
                            target: "tempo::cli",
                            %address,
                            progress = format_args!("{}/{} ({pct:.0}%)", i + 1, pair_count),
                            elapsed = ?elapsed,
                            pairs_per_sec = pairs_per_sec as u64,
                            "Inserting storage"
                        );
                        last_log = now;
                    }
                }

                provider_rw.insert_storage_for_hashing(batch.drain())?;
                storage_entries = batch.entry(address).or_default();
            }

            // Drop cursor before potential flush at top of next iteration
            drop(storage_cursor);

            total_blocks += 1;
        }

        // Flush any remaining batch entries
        if !batch.is_empty() {
            provider_rw.insert_storage_for_hashing(batch.drain())?;
        }

        info!(
            target: "tempo::cli",
            total_blocks,
            unique_tokens = accounts_seen.len(),
            total_entries,
            "Plain and hashed storage written, writing hashed accounts..."
        );

        // Write hashed account entries using the real account metadata from plain state.
        // This preserves bytecode_hash for genesis accounts (e.g. TIP20 tokens with 0xEF code).
        provider_rw.insert_account_for_hashing(
            accounts_seen
                .iter()
                .map(|(addr, account)| (*addr, Some(*account))),
        )?;

        info!(
            target: "tempo::cli",
            addresses = accounts_seen.len(),
            "Hashed accounts written"
        );

        // Final commit
        provider_rw.commit()?;

        info!(
            target: "tempo::cli",
            total_blocks,
            unique_tokens = accounts_seen.len(),
            total_entries,
            "Binary state dump loaded successfully"
        );

        // Note: The state root will need to be recomputed.
        // The node will compute the actual state root on startup.
        info!(
            target: "tempo::cli",
            "State loaded. The node will compute the state root on startup."
        );

        Ok(())
    }
}
